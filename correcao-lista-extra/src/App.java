public class App {
    public static void main(String[] args) throws Exception {
        System.out.printf("O valor %d é inteiro\n", 1);
        System.out.printf("O valor %f é double\n", 1.02);
        System.out.printf("O valor %f é float\n", 1.02f);
        System.out.printf("O valor %.2f é float com duas casas decimais\n", 1.02f);
        System.out.printf("O valor %c é char\n", 'c');
        System.out.printf("O valor %s é string\n", "Olá");
        System.out.printf("O valor %S é string\n", "Olá");
        System.out.printf("O valor %b é boolean\n", true);
    }
}
